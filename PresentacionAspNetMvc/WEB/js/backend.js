$(function () {
    $('table').DataTable();

    ocultarTodos();

    $('#btnProductos').click(function () {
        ocultarTodos();
        $('#productos').show();
    });

    $('#btnUsuarios').click(function () {
        ocultarTodos();
        $('#usuarios').show();
    });
});

function ocultarTodos() {
    $('section').hide();
}